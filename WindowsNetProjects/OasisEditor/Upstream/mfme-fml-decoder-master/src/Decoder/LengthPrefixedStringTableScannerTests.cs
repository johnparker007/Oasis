using MfmeFmlDecoder.Decoder;
using System;
using System.IO;
using System.Text;
using Xunit;

namespace MfmeFmlDecoder.src.Decoder
{
    public class LengthPrefixedStringTableScannerTests
    {
        [Fact]
        public void SkipContinuation_StopsBeforeNextFileLevelTag()
        {
            byte[] data =
            {
                0x43, 0x00, 0x00, 0x00,
                0x19, 0x00, 0x00, 0x00,
                0xA3, 0x31, 0x20, 0x50, 0x52, 0x49, 0x4F, 0x52, 0x49, 0x54, 0x59, 0x20, 0x50, 0x41, 0x59, 0x4F, 0x55, 0x54, 0x20, 0x4C, 0x4F, 0x47, 0x49, 0x43, 0x00,
                0x1D, 0x00, 0x00, 0x00,
                0x53, 0x31, 0x30, 0x20, 0x53, 0x49, 0x4E, 0x47, 0x4C, 0x45, 0x20, 0x43, 0x4F, 0x49, 0x4E, 0x20, 0x45, 0x4E, 0x54, 0x52, 0x59, 0x20, 0x4F, 0x50, 0x54, 0x49, 0x4F, 0x4E, 0x00,
                0x09, 0x00, 0x00, 0x00, 0x4E, 0x4F, 0x54, 0x20, 0x55, 0x53, 0x45, 0x44, 0x00,
                0x44, 0x00, 0x00, 0x00,
                0x1A, 0x00, 0x00, 0x00,
                0x41, 0x00,
            };

            using var stream = new MemoryStream(data);
            using var reader = new BinaryReader(stream, Encoding.UTF8, leaveOpen: true);

            reader.ReadUInt32();
            uint firstStringLength = reader.ReadUInt32();
            reader.ReadBytes(checked((int)firstStringLength));

            LengthPrefixedStringTableScanner.SkipContinuation(reader, hostTagKeyByte: 0x43, scanEndExclusive: stream.Length);

            Assert.Equal(0x4F, stream.Position);
        }

        [Fact]
        public void MeasureSpan_MatchesNestedExtendedTagLayout()
        {
            byte[] data =
            {
                0x03, 0x00, 0x00, 0x00,
                0xA3, 0x31, 0x00,
                0x44, 0x00, 0x00, 0x00,
            };

            int spanBytes = LengthPrefixedStringTableScanner.MeasureSpan(data, offset: 0, hostTagKeyByte: 0x43);

            Assert.Equal(7, spanBytes);
        }
    }
}
